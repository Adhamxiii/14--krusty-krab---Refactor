const selectLanguage = document.querySelector("#translate");
const body = document.body;

selectLanguage.addEventListener("change", () => {
  const selectedLanguage = selectLanguage.value;

  const elementsToTranslate = document.querySelectorAll("[data-translate]");
  elementsToTranslate.forEach((element) => {
    const translationKey = element.getAttribute("data-translate");
    if (
      translate[selectedLanguage] &&
      translate[selectedLanguage][translationKey]
    ) {
      element.textContent = translate[selectedLanguage][translationKey];
    }
  });

  if (selectedLanguage === "ar") {
    body.classList.add("arabic");
  } else {
    body.classList.remove("arabic");
  }
});
